package fr.diginamic.bo;

/**
 * Enumération représentant les environnements de vie possibles pour un poisson.
 * Un poisson peut vivre en eau douce (FRESH_WATER) ou en eau de mer (SEA_WATER).
 */
public enum FishLivEnv {

    /** Eau douce */
    FRESH_WATER,

    /** Eau de mer */
    SEA_WATER;
}