package fr.diginamic.bo;

/**
 * Enumération représentant les types de produits disponibles dans une animalerie.
 * Un produit peut être de type alimentation (FOOD), accessoire (ACCESSORY) ou nettoyage (CLEANING).
 */
public enum ProdType {

    /** Alimentation */
    FOOD,

    /** Accessoire */
    ACCESSORY,

    /** Nettoyage */
    CLEANING
}